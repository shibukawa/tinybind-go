package sqlbind

import (
	"bytes"
	"fmt"
	"go/format"
	"strconv"
	"strings"
	"unicode"
)

// GenerateOptions controls SQL code generation.
type GenerateOptions struct {
	Package string
	// PlaceholderStyle is "dollar" (the PostgreSQL default) or "question".
	PlaceholderStyle string
}

// Generate parses, validates, and compiles a SQL template module to Go.
func Generate(filename string, source []byte, options GenerateOptions) ([]byte, error) {
	module, err := Parse(filename, source)
	if err != nil {
		return nil, err
	}
	c := newCompiler(filename, module)
	if err := c.analyze(); err != nil {
		return nil, err
	}
	if options.PlaceholderStyle == "" {
		options.PlaceholderStyle = "dollar"
	}
	if options.PlaceholderStyle != "dollar" && options.PlaceholderStyle != "question" {
		return nil, fmt.Errorf("unsupported SQL placeholder style %q", options.PlaceholderStyle)
	}
	generated, err := c.emit(options)
	if err != nil {
		return nil, err
	}
	formatted, err := format.Source(generated)
	if err != nil {
		return generated, fmt.Errorf("format generated SQL code: %w\n%s", err, generated)
	}
	return formatted, nil
}

type goEmitter struct {
	c      *compiler
	b      bytes.Buffer
	indent int
	style  string
}

func (c *compiler) emit(options GenerateOptions) ([]byte, error) {
	e := &goEmitter{c: c, style: options.PlaceholderStyle}
	pkg := options.Package
	if pkg == "" && c.module.Package != nil {
		pkg = c.module.Package.Name
		if i := strings.LastIndex(pkg, "."); i >= 0 {
			pkg = pkg[i+1:]
		}
	}
	if pkg == "" {
		pkg = "templates"
	}
	pkg = goIdentifier(pkg)
	e.b.WriteString("// Code generated by tinybind SQL templates; DO NOT EDIT.\n\n")
	fmt.Fprintf(&e.b, "package %s\n\n", pkg)
	e.emitImports()
	e.emitRuntime()
	e.emitDeclaredTypes()
	for _, declaration := range c.module.Declarations {
		if statement, ok := declaration.(*TemplateDecl); ok {
			if err := e.emitStatement(statement); err != nil {
				return nil, err
			}
		}
	}
	return e.b.Bytes(), nil
}

func (e *goEmitter) emitImports() {
	executable := false
	for _, s := range e.c.statements {
		if s.decl.Exported && s.cardinality != "predicate" && s.cardinality != "relation" {
			executable = true
		}
	}
	e.b.WriteString("import (\n")
	if executable {
		e.b.WriteString("\t\"context\"\n\t\"database/sql\"\n")
	}
	e.b.WriteString("\t\"fmt\"\n")
	if e.style == "dollar" {
		e.b.WriteString("\t\"strconv\"\n")
	}
	e.b.WriteString("\t\"strings\"\n")
	if e.c.usesKind(kindDateTime) || e.c.usesKind(kindDate) || e.c.usesKind(kindTime) {
		e.b.WriteString("\t\"time\"\n")
	}
	if e.c.usesKind(kindURL) {
		e.b.WriteString("\t\"net/url\"\n")
	}
	e.b.WriteString(")\n\n")
}

func (e *goEmitter) emitRuntime() {
	e.b.WriteString("type Statement struct { SQL string; Args []any }\n\n")
	e.b.WriteString("type _tinybindSQLBuilder struct { strings.Builder; args []any }\n")
	if e.style == "question" {
		e.b.WriteString("func (b *_tinybindSQLBuilder) arg(value any) { b.WriteByte('?'); b.args = append(b.args, value) }\n")
	} else {
		e.b.WriteString("func (b *_tinybindSQLBuilder) arg(value any) { b.args = append(b.args, value); b.WriteByte('$'); b.WriteString(strconv.Itoa(len(b.args))) }\n")
	}
	e.b.WriteString("func _tinybindSQLArgs[T any](b *_tinybindSQLBuilder, values []T) error { if len(values) == 0 { return fmt.Errorf(\"tinybind SQL: an expanded value list cannot be empty\") }; for i, value := range values { if i > 0 { b.WriteString(\", \") }; b.arg(value) }; return nil }\n")
	e.b.WriteString("func _tinybindStatement(b *_tinybindSQLBuilder) Statement { return Statement{SQL: b.String(), Args: b.args} }\n")
	e.b.WriteString(`func _tinybindSafeMutation(query string) bool {
	words := strings.Fields(strings.ToUpper(query))
	mutation := len(words) > 0 && (words[0] == "UPDATE" || words[0] == "DELETE")
	if !mutation { return true }
	where := -1
	set := -1
	for i, word := range words { switch strings.Trim(word, "(),;") { case "SET": set = i; case "WHERE": where = i } }
	if where < 0 || where+1 >= len(words) { return false }
	next := strings.Trim(words[where+1], "(),;")
	if next == "" || next == "RETURNING" || next == "ORDER" || next == "GROUP" || next == "LIMIT" || next == "OFFSET" { return false }
	return words[0] != "UPDATE" || (set >= 0 && set+1 < where)
}

`)
	executable := false
	for _, s := range e.c.statements {
		if s.decl.Exported && s.cardinality != "predicate" && s.cardinality != "relation" {
			executable = true
		}
	}
	if executable {
		e.b.WriteString("type SQLExecer interface { ExecContext(context.Context, string, ...any) (sql.Result, error) }\n")
		e.b.WriteString("type SQLQuerier interface { QueryContext(context.Context, string, ...any) (*sql.Rows, error) }\n\n")
	}
}

func (e *goEmitter) emitDeclaredTypes() {
	for _, declaration := range e.c.module.Declarations {
		switch d := declaration.(type) {
		case *TypeDecl:
			fmt.Fprintf(&e.b, "type %s struct {\n", d.Name)
			for _, f := range d.Fields {
				t, _ := e.c.resolveType(f.Type)
				fmt.Fprintf(&e.b, "\t%s %s\n", goPublicName(f.Name), goType(t))
			}
			e.b.WriteString("}\n\n")
		case *EnumDecl:
			fmt.Fprintf(&e.b, "type %s string\n\nconst (\n", d.Name)
			for _, m := range d.Members {
				fmt.Fprintf(&e.b, "\t%s%s %s = %q\n", d.Name, m.Name, d.Name, m.Name)
			}
			e.b.WriteString(")\n\n")
		}
	}
}

func (e *goEmitter) emitStatement(statement *TemplateDecl) error {
	info := e.c.statements[statement.Name]
	internal := "_tinybindBuild" + statement.Name
	fmt.Fprintf(&e.b, "func %s(b *_tinybindSQLBuilder", internal)
	for _, p := range statement.Parameters {
		t, _ := e.c.resolveType(p.Type)
		fmt.Fprintf(&e.b, ", %s %s", goLocalName(p.Name), goType(t))
	}
	e.b.WriteString(") error {\n")
	e.indent = 1
	if err := e.emitNodes(statement.Body.([]Node), info.params); err != nil {
		return err
	}
	e.line("return nil")
	e.b.WriteString("}\n\n")
	if !statement.Exported {
		return nil
	}
	fmt.Fprintf(&e.b, "func Build%s(", statement.Name)
	e.emitParams(statement.Parameters)
	e.b.WriteString(") (Statement, error) {\n\tvar b _tinybindSQLBuilder\n\tif err := " + internal + "(&b")
	for _, p := range statement.Parameters {
		e.b.WriteString(", " + goLocalName(p.Name))
	}
	e.b.WriteString("); err != nil { return Statement{}, err }\n")
	if isMutation(statement.Body.([]Node)) {
		e.b.WriteString("\tif !_tinybindSafeMutation(b.String()) { return Statement{}, fmt.Errorf(\"tinybind SQL: UPDATE and DELETE require a non-empty WHERE clause\") }\n")
	}
	e.b.WriteString("\treturn _tinybindStatement(&b), nil\n}\n\n")
	switch info.cardinality {
	case "exec":
		e.emitExecAPI(statement)
	case "one", "optional", "many":
		e.emitQueryAPI(statement, info)
	}
	return nil
}

func (e *goEmitter) emitParams(params []Parameter) {
	for i, p := range params {
		if i > 0 {
			e.b.WriteString(", ")
		}
		t, _ := e.c.resolveType(p.Type)
		fmt.Fprintf(&e.b, "%s %s", goLocalName(p.Name), goType(t))
	}
}
func (e *goEmitter) callParams(params []Parameter) string {
	var out []string
	for _, p := range params {
		out = append(out, goLocalName(p.Name))
	}
	if len(out) == 0 {
		return ""
	}
	return ", " + strings.Join(out, ", ")
}

func (e *goEmitter) emitExecAPI(statement *TemplateDecl) {
	fmt.Fprintf(&e.b, "func %s(ctx context.Context, db SQLExecer", statement.Name)
	for _, p := range statement.Parameters {
		t, _ := e.c.resolveType(p.Type)
		fmt.Fprintf(&e.b, ", %s %s", goLocalName(p.Name), goType(t))
	}
	e.b.WriteString(") (sql.Result, error) {\n")
	fmt.Fprintf(&e.b, "\tstatement, err := Build%s(%s)\n", statement.Name, strings.TrimPrefix(e.callParams(statement.Parameters), ", "))
	e.b.WriteString("\tif err != nil { return nil, err }\n\treturn db.ExecContext(ctx, statement.SQL, statement.Args...)\n}\n\n")
}

func (e *goEmitter) emitQueryAPI(statement *TemplateDecl, info *statementInfo) {
	result := goType(info.result)
	returnType := result
	if info.cardinality == "optional" {
		returnType = "*" + result
	} else if info.cardinality == "many" {
		returnType = "[]" + result
	}
	fmt.Fprintf(&e.b, "func %s(ctx context.Context, db SQLQuerier", statement.Name)
	for _, p := range statement.Parameters {
		t, _ := e.c.resolveType(p.Type)
		fmt.Fprintf(&e.b, ", %s %s", goLocalName(p.Name), goType(t))
	}
	fmt.Fprintf(&e.b, ") (%s, error) {\n", returnType)
	fmt.Fprintf(&e.b, "\tstatement, err := Build%s(%s)\n", statement.Name, strings.TrimPrefix(e.callParams(statement.Parameters), ", "))
	zero := result + "{}"
	if info.cardinality == "optional" {
		zero = "nil"
	} else if info.cardinality == "many" {
		zero = "nil"
	}
	fmt.Fprintf(&e.b, "\tif err != nil { return %s, err }\n", zero)
	e.b.WriteString("\trows, err := db.QueryContext(ctx, statement.SQL, statement.Args...)\n")
	fmt.Fprintf(&e.b, "\tif err != nil { return %s, err }\n\tdefer rows.Close()\n", zero)
	if info.cardinality == "many" {
		fmt.Fprintf(&e.b, "\tvar results []%s\n\tfor rows.Next() {\n\t\tvar result %s\n", result, result)
		e.emitScan("\t\t", info.result, "result", zero)
		e.b.WriteString("\t\tresults = append(results, result)\n\t}\n\tif err := rows.Err(); err != nil { return nil, err }\n\treturn results, nil\n}\n\n")
		return
	}
	fmt.Fprintf(&e.b, "\tif !rows.Next() { if err := rows.Err(); err != nil { return %s, err }; ", zero)
	if info.cardinality == "one" {
		fmt.Fprintf(&e.b, "return %s, sql.ErrNoRows", zero)
	} else {
		e.b.WriteString("return nil, nil")
	}
	e.b.WriteString(" }\n")
	fmt.Fprintf(&e.b, "\tvar result %s\n", result)
	e.emitScan("\t", info.result, "result", zero)
	fmt.Fprintf(&e.b, "\tif rows.Next() { return %s, fmt.Errorf(\"tinybind SQL: %s expected at most one row\") }\n", zero, statement.Name)
	fmt.Fprintf(&e.b, "\tif err := rows.Err(); err != nil { return %s, err }\n", zero)
	if info.cardinality == "optional" {
		e.b.WriteString("\treturn &result, nil\n}\n\n")
	} else {
		e.b.WriteString("\treturn result, nil\n}\n\n")
	}
}

func (e *goEmitter) emitScan(indent string, t valueType, target, zero string) {
	record := e.c.records[t.name]
	var fields []string
	for _, f := range record.Fields {
		fields = append(fields, "&"+target+"."+goPublicName(f.Name))
	}
	fmt.Fprintf(&e.b, "%sif err := rows.Scan(%s); err != nil { return %s, err }\n", indent, strings.Join(fields, ", "), zero)
}

func (e *goEmitter) emitNodes(nodes []Node, scope map[string]valueType) error {
	for _, node := range nodes {
		switch n := node.(type) {
		case *TextNode:
			e.line("b.WriteString(" + strconv.Quote(n.Text) + ")")
		case *ExpressionNode:
			code, err := e.expr(n.Expression, scope)
			if err != nil {
				return err
			}
			t := e.c.exprTypes[n.Expression]
			if t.kind == kindPredicate {
				call := n.Expression.(*CallExpr)
				id := call.Callee.(*IdentifierExpr)
				var args []string
				for _, argument := range call.Arguments {
					value, err := e.expr(argument, scope)
					if err != nil {
						return err
					}
					args = append(args, value)
				}
				suffix := ""
				if len(args) > 0 {
					suffix = ", " + strings.Join(args, ", ")
				}
				e.line("if err := _tinybindBuild" + id.Name + "(b" + suffix + "); err != nil { return err }")
			} else if t.kind == kindArray {
				if t.optional {
					return e.c.error(n.Pos, "optional arrays cannot be expanded as SQL values")
				}
				e.line("if err := _tinybindSQLArgs(b, " + code + "); err != nil { return err }")
			} else {
				e.line("b.arg(" + code + ")")
			}
		case *RelationNode:
			e.line("b.WriteByte('(')")
			var args []string
			for _, argument := range n.Arguments {
				value, err := e.expr(argument, scope)
				if err != nil {
					return err
				}
				args = append(args, value)
			}
			suffix := ""
			if len(args) > 0 {
				suffix = ", " + strings.Join(args, ", ")
			}
			e.line("if err := _tinybindBuild" + n.Name + "(b" + suffix + "); err != nil { return err }")
			e.line("b.WriteString(" + strconv.Quote(") AS "+n.Alias) + ")")
		case *IfNode:
			condition, err := e.expr(n.Condition, scope)
			if err != nil {
				return err
			}
			e.line("if " + condition + " {")
			e.indent++
			if err := e.emitNodes(n.Then, copyScope(scope)); err != nil {
				return err
			}
			e.indent--
			if len(n.Else) > 0 {
				e.line("} else {")
				e.indent++
				if err := e.emitNodes(n.Else, copyScope(scope)); err != nil {
					return err
				}
				e.indent--
				e.line("}")
			} else {
				e.line("}")
			}
		}
	}
	return nil
}

func (e *goEmitter) line(value string) {
	e.b.WriteString(strings.Repeat("\t", e.indent))
	e.b.WriteString(value)
	e.b.WriteByte('\n')
}
func (e *goEmitter) expr(expr Expr, scope map[string]valueType) (string, error) {
	switch x := expr.(type) {
	case *IdentifierExpr:
		if t, ok := e.c.enumMembers[x.Name]; ok {
			return t.name + x.Name, nil
		}
		return goLocalName(x.Name), nil
	case *LiteralExpr:
		switch x.ValueKind {
		case "string":
			return strconv.Quote(x.Value.(string)), nil
		case "bool":
			return strconv.FormatBool(x.Value.(bool)), nil
		case "number":
			return x.Value.(string), nil
		case "null":
			return "nil", nil
		}
	case *MemberExpr:
		o, err := e.expr(x.Object, scope)
		if err != nil {
			return "", err
		}
		return o + "." + goPublicName(x.Member), nil
	case *IndexExpr:
		o, err := e.expr(x.Object, scope)
		if err != nil {
			return "", err
		}
		i, err := e.expr(x.Index, scope)
		if err != nil {
			return "", err
		}
		return o + "[" + i + "]", nil
	case *CallExpr:
		id := x.Callee.(*IdentifierExpr)
		var args []string
		for _, a := range x.Arguments {
			code, err := e.expr(a, scope)
			if err != nil {
				return "", err
			}
			args = append(args, code)
		}
		return id.Name + "(" + strings.Join(args, ", ") + ")", nil
	case *UnaryExpr:
		o, err := e.expr(x.Operand, scope)
		if err != nil {
			return "", err
		}
		op := x.Operator
		if op == "not" {
			op = "!"
		}
		return op + "(" + o + ")", nil
	case *BinaryExpr:
		l, err := e.expr(x.Left, scope)
		if err != nil {
			return "", err
		}
		r, err := e.expr(x.Right, scope)
		if err != nil {
			return "", err
		}
		op := x.Operator
		if op == "and" {
			op = "&&"
		} else if op == "or" {
			op = "||"
		}
		return "(" + l + " " + op + " " + r + ")", nil
	case *ConditionalExpr:
		c, err := e.expr(x.Condition, scope)
		if err != nil {
			return "", err
		}
		a, err := e.expr(x.Then, scope)
		if err != nil {
			return "", err
		}
		b, err := e.expr(x.Else, scope)
		if err != nil {
			return "", err
		}
		return "(func() " + goType(e.c.exprTypes[expr]) + " { if " + c + " { return " + a + " }; return " + b + " })()", nil
	}
	return "", fmt.Errorf("unsupported expression %T", expr)
}

func goType(t valueType) string {
	var base string
	switch t.kind {
	case kindString, kindDecimal:
		base = "string"
	case kindBool:
		base = "bool"
	case kindInt:
		base = "int"
	case kindFloat:
		base = "float64"
	case kindBytes:
		base = "[]byte"
	case kindDateTime, kindDate, kindTime:
		base = "time.Time"
	case kindURL:
		base = "url.URL"
	case kindRecord, kindEnum:
		base = t.name
	case kindArray:
		if t.elem != nil {
			base = "[]" + goType(*t.elem)
		}
	}
	if t.optional {
		return "*" + base
	}
	return base
}
func goIdentifier(value string) string {
	var out strings.Builder
	for i, r := range value {
		if unicode.IsLetter(r) || r == '_' || (i > 0 && unicode.IsDigit(r)) {
			out.WriteRune(r)
		} else {
			out.WriteRune('_')
		}
	}
	if out.Len() == 0 {
		return "templates"
	}
	return goLocalName(out.String())
}
func goLocalName(name string) string {
	if goKeywords[name] {
		return "_" + name
	}
	return name
}
func goPublicName(name string) string {
	if name == "" {
		return name
	}
	runes := []rune(name)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

var goKeywords = map[string]bool{"break": true, "default": true, "func": true, "interface": true, "select": true, "case": true, "defer": true, "go": true, "map": true, "struct": true, "chan": true, "else": true, "goto": true, "package": true, "switch": true, "const": true, "fallthrough": true, "if": true, "range": true, "type": true, "continue": true, "for": true, "import": true, "return": true, "var": true}
