package generator

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strconv"
)

// GenerateOpenAPI builds OpenAPI 3.1 from Go sources in dir and writes a generated
// file that embeds the document and registers it with httpbinder for serving.
func GenerateOpenAPI(dir, outDir, outName string) (string, error) {
	return New(DefaultOptions()).GenerateOpenAPI(dir, outDir, outName)
}

// GenerateOpenAPI writes OpenAPI generated with this generator's identities.
func (g *Generator) GenerateOpenAPI(dir, outDir, outName string) (string, error) {
	doc, err := g.BuildOpenAPI(dir)
	if err != nil {
		return "", err
	}
	plan, err := g.Analyze(dir)
	if err != nil {
		return "", err
	}
	src, err := EmitOpenAPI(plan.Package, doc)
	if err != nil {
		return "", err
	}
	if outDir == "" {
		outDir = dir
	}
	if outName == "" {
		outName = "httpbinder_openapi_gen.go"
	}
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return "", err
	}
	path := filepath.Join(outDir, outName)
	if err := os.WriteFile(path, src, 0o644); err != nil {
		return "", err
	}
	abs, err := filepath.Abs(path)
	if err != nil {
		return path, nil
	}
	return abs, nil
}

// EmitOpenAPI produces Go source embedding OpenAPI JSON/YAML and registering it.
func EmitOpenAPI(pkg string, doc Document) ([]byte, error) {
	if pkg == "" {
		pkg = "main"
	}
	jsonBytes, err := doc.JSON()
	if err != nil {
		return nil, err
	}
	yamlBytes, err := doc.YAML()
	if err != nil {
		return nil, err
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by httpbinder; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("import \"github.com/shibukawa/httpbind-go\"\n\n")
	b.WriteString("func init() {\n")
	b.WriteString("\thttpbinder.RegisterOpenAPI([]byte(openapiJSON), []byte(openapiYAML))\n")
	b.WriteString("}\n\n")
	fmt.Fprintf(&b, "const openapiJSON = %s\n\n", strconv.Quote(string(jsonBytes)))
	fmt.Fprintf(&b, "const openapiYAML = %s\n", strconv.Quote(string(yamlBytes)))

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return b.Bytes(), fmt.Errorf("format openapi gen: %w\n%s", err, b.String())
	}
	return formatted, nil
}
