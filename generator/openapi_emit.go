package generator

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strconv"
)

// GenerateOpenAPI builds OpenAPI 3.1 from Go sources in dir and writes a generated
// file that embeds the document and registers it with httpbind for serving.
func GenerateOpenAPI(dir, outDir, outName string) (string, error) {
	return New(DefaultOptions()).GenerateOpenAPI(dir, outDir, outName)
}

// GenerateOpenAPI writes OpenAPI generated with this generator's identities.
func (g *Generator) GenerateOpenAPI(dir, outDir, outName string) (string, error) {
	doc, err := g.BuildOpenAPI(dir)
	if err != nil {
		return "", err
	}
	plan, err := g.Analyze(dir)
	if err != nil {
		return "", err
	}
	src, err := EmitOpenAPIFragment(plan.Package, plan.PackagePath, doc)
	if err != nil {
		return "", err
	}
	if outDir == "" {
		outDir = dir
	}
	if outName == "" {
		outName = "tinybind_openapi_gen.go"
	}
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return "", err
	}
	path := filepath.Join(outDir, outName)
	if err := os.WriteFile(path, src, 0o644); err != nil {
		return "", err
	}
	abs, err := filepath.Abs(path)
	if err != nil {
		return path, nil
	}
	return abs, nil
}

// EmitOpenAPI produces Go source for a package-named fragment. Prefer
// EmitOpenAPIFragment when the package import path is available.
func EmitOpenAPI(pkg string, doc Document) ([]byte, error) {
	return EmitOpenAPIFragment(pkg, pkg, doc)
}

// EmitOpenAPIFragment produces Go source embedding and registering one
// package-local OpenAPI fragment.
func EmitOpenAPIFragment(pkg, packagePath string, doc Document) ([]byte, error) {
	if pkg == "" {
		pkg = "main"
	}
	if packagePath == "" {
		packagePath = pkg
	}
	fragment := Document{
		"paths":      doc["paths"],
		"components": doc["components"],
	}
	jsonBytes, err := fragment.JSON()
	if err != nil {
		return nil, err
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by tinybind; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("import \"github.com/shibukawa/tinybind-go\"\n\n")
	b.WriteString("func init() {\n")
	fmt.Fprintf(&b, "\thttpbind.RegisterOpenAPIFragment(%s, []byte(openapiFragmentJSON))\n", strconv.Quote(packagePath))
	b.WriteString("}\n\n")
	fmt.Fprintf(&b, "const openapiFragmentJSON = %s\n", strconv.Quote(string(jsonBytes)))

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return b.Bytes(), fmt.Errorf("format openapi gen: %w\n%s", err, b.String())
	}
	return formatted, nil
}
