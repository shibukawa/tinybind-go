// Package codegen emits Go source that constructs cliparser.Def slices for Bind-style fields.
package codegen

import (
	"bytes"
	"fmt"
	"go/format"
	"strconv"

	"github.com/shibukawa/tinybind-go/cliparser"
)

// Generate emits a Go file defining a function that returns []cliparser.Def built from fields.
// Field naming is resolved at generate time via cliparser.BuildDefs (same path runtime uses).
func Generate(packageName, funcName string, fields []cliparser.FieldMeta) ([]byte, error) {
	if packageName == "" {
		return nil, fmt.Errorf("cliparser/codegen: package name is required")
	}
	if funcName == "" {
		funcName = "FlagDefs"
	}
	defs, err := cliparser.BuildDefs(fields)
	if err != nil {
		return nil, err
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by cliparser/codegen; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", packageName)
	b.WriteString("import \"github.com/shibukawa/tinybind-go/cliparser\"\n\n")
	fmt.Fprintf(&b, "// %s returns precomputed CLI flag definitions for generated code.\n", funcName)
	fmt.Fprintf(&b, "func %s() []cliparser.Def {\n", funcName)
	b.WriteString("\treturn []cliparser.Def{\n")
	for _, d := range defs {
		b.WriteString("\t\t{\n")
		fmt.Fprintf(&b, "\t\t\tConfigKey: %s,\n", strconv.Quote(d.ConfigKey))
		b.WriteString("\t\t\tLongs: []string{")
		for i, ln := range d.Longs {
			if i > 0 {
				b.WriteString(", ")
			}
			b.WriteString(strconv.Quote(ln))
		}
		b.WriteString("},\n")
		if len(d.Shorts) > 0 {
			b.WriteString("\t\t\tShorts: []string{")
			for i, sh := range d.Shorts {
				if i > 0 {
					b.WriteString(", ")
				}
				b.WriteString(strconv.Quote(sh))
			}
			b.WriteString("},\n")
		}
		if d.Help != "" {
			fmt.Fprintf(&b, "\t\t\tHelp: %s,\n", strconv.Quote(d.Help))
		}
		fmt.Fprintf(&b, "\t\t\tKind: cliparser.%s,\n", kindConst(d.Kind))
		if d.UsesOptOverride {
			b.WriteString("\t\t\tUsesOptOverride: true,\n")
		}
		b.WriteString("\t\t},\n")
	}
	b.WriteString("\t}\n")
	b.WriteString("}\n")

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return b.Bytes(), fmt.Errorf("cliparser/codegen: format: %w\n%s", err, b.String())
	}
	return formatted, nil
}

func kindConst(k cliparser.Kind) string {
	switch k {
	case cliparser.KindBool:
		return "KindBool"
	case cliparser.KindArray:
		return "KindArray"
	default:
		return "KindString"
	}
}

